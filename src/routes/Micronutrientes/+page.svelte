    <div class="caixa">
      <h3>Principais Micronutrientes, Funções e Ingestão Recomendada</h3>
      <ul>
        <li><strong>Vitamina A:</strong> Essencial para a visão, saúde da pele e função imunológica. (RDA: 700–900 µg RAE/dia)</li>
        <li><strong>Vitamina C:</strong> Antioxidante que melhora a absorção de ferro e fortalece as defesas. (RDA: 75–90 mg/dia)</li>
        <li><strong>Vitamina D:</strong> Importante para a absorção de cálcio e saúde óssea. (RDA: 600–800 IU/dia)</li>
        <li><strong>Vitamina E:</strong> Protege as células contra danos oxidativos. (RDA: 15 mg/dia)</li>
        <li><strong>Vitamina K:</strong> Necessária para a coagulação sanguínea e saúde dos ossos. (AI: 90–120 µg/dia)</li>
        <li><strong>Cálcio:</strong> Fundamental para ossos e dentes fortes, além da função muscular. (RDA: ~1000 mg/dia)</li>
        <li><strong>Ferro:</strong> Essencial para a produção de hemoglobina e transporte de oxigênio. (RDA: ~8 mg/dia para homens e ~18 mg/dia para mulheres)</li>
        <li><strong>Magnésio:</strong> Participa de diversas reações enzimáticas e na produção de energia. (RDA: ~320–420 mg/dia)</li>
        <li><strong>Zinco:</strong> Importante para o sistema imunológico e síntese proteica. (RDA: ~8–11 mg/dia)</li>
        <li><strong>Potássio:</strong> Auxilia no equilíbrio hídrico e funcionamento muscular. (AI: 4700 mg/dia)</li>
      </ul>
    </div>

    <div class="caixa">
      <h3>Consumo de Alimentos Naturais</h3>
      <p>Optar por alimentos naturais, como frutas, vegetais, carnes e leite, é a melhor estratégia para garantir a ingestão adequada de micronutrientes. Alimentos minimamente processados preservam os nutrientes essenciais, oferecendo uma variedade de vitaminas e minerais que são perdidos em produtos industrializados.</p>
      <p>Além disso, estudos indicam que uma grande parte da população americana não atinge os valores recomendados para certos micronutrientes. Por exemplo, conforme demonstrado no estudo de Reider et al. (2020) publicado na revista <em>Nutrients</em> (<a href="https://pmc.ncbi.nlm.nih.gov/articles/PMC7352522/" target="_blank" style="color: #ffd700;">PMC7352522</a>), 45% dos americanos apresentam inadequação na ingestão de vitamina A, 46% para vitamina C, 95% para vitamina D, 84% para vitamina E e 15% para zinco. Esses dados reforçam a importância de consumir alimentos naturais para atingir a ingestão necessária desses nutrientes.</p>
